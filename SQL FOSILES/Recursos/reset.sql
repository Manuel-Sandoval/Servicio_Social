CREATE TABLE FOSILES.FOSILES (
    ID_FOSIL NUMBER,
    MUESTRA VARCHAR2(10),
    GRUPO VARCHAR2(100),
    NOMBRE VARCHAR2(100),
    ID_PAIS NUMBER,
    ID_ESTADO NUMBER,
    ID_MUNICIPIO NUMBER,
